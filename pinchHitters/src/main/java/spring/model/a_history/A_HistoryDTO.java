package spring.model.a_history;

public class A_HistoryDTO {
	private int ah_num;
	private String ah_name;
	private String ah_date;
	private int ah_time;
	private int ah_endtime;
	private String ah_salary;
	private String u_ID;
	private String c_ID;

	public int getAh_num() {
		return ah_num;
	}

	public void setAh_num(int ah_num) {
		this.ah_num = ah_num;
	}

	public String getAh_name() {
		return ah_name;
	}

	public void setAh_name(String ah_name) {
		this.ah_name = ah_name;
	}

	public String getAh_date() {
		return ah_date;
	}

	public void setAh_date(String ah_date) {
		this.ah_date = ah_date;
	}

	public int getAh_time() {
		return ah_time;
	}

	public void setAh_time(int ah_time) {
		this.ah_time = ah_time;
	}

	public int getAh_endtime() {
		return ah_endtime;
	}

	public void setAh_endtime(int ah_endtime) {
		this.ah_endtime = ah_endtime;
	}

	public String getAh_salary() {
		return ah_salary;
	}

	public void setAh_salary(String ah_salary) {
		this.ah_salary = ah_salary;
	}

	public String getU_ID() {
		return u_ID;
	}

	public void setU_ID(String u_ID) {
		this.u_ID = u_ID;
	}

	public String getC_ID() {
		return c_ID;
	}

	public void setC_ID(String c_ID) {
		this.c_ID = c_ID;
	}
}
